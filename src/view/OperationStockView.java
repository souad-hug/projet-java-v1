    private void initProduits() {
        for (int i = 1; i <= 10; i++) {
            Produit p = new Produit("prd" + i, "ref" + i, new Long(i));
            produits.add(p);
            jComboBox2.addItem(p.getLibelle());
        }
    }

    private void initMagasins() {
        for (int i = 1; i <= 10; i++) {
            Magasin m = new Magasin(new Long(i), "mag" + i);
            magasins.add(m);
            jComboBox1.addItem(m.getLibelle());
        }
    }



